<div class="row">
	<div class="col-md-12">
		<p>Pertama - tama kita harus mengenali dulu, apa itu psikolog dan apa itu psikiater. Sebenarnya psikolog maupun psikiater memiliki tugas yang sama  yaitu sama-sama menangani permasalahan seputar kesehatan mental, tapi keduanya memiliki perbedaan cara penanganan maupun latar belakang pendidikan.</p>
	</div>
</div>
<div class="row">
	<div class="col-md-12">
		<h3><strong>Psikolog</strong></h3>
		<p>Psikolog merupakan seorang yang ahli dalam ilmu psikologi yang berfokus pada pikiran dan perilaku seseorang. Psikolog umumnya menggunakan psikoterapi untuk membantu klien atau pasien untuk mengatasi masalah yang memengaruhi kondisi mental dan kesehatannya. Dalam menyelesaikan masalah psikologis ataupun memperbaiki perilaku pasien, psikolog dapat bekerjasama dengan psikiater dan dokter yang menangani pasien. Kerjasama dilakukan apabila pasien membutuhkan pengobatan sekaligus psikoterapi dan konseling dari psikolog.
		Psikolog umum dapat mendalami bidang psikologi yang berbeda-beda setelah lulus program pendidikan sarjana psikologi (S1 atau S.Psi). Bidang psikologi yang dapat dipelajari lebih dalam ada banyak, termasuk psikologi industri dan organisasi, psikologi klinis anak dan dewasa, serta psikologi pendidikan. Setelah menyelesaikan pendidikan lebih lanjut tersebut, psikolog akan memiliki gelar Magister Psikologi (M.Psi).
		</p>
	</div>
</div>
<div class="row">
	<div class="col-md-12">
		<h3><strong>Psikiater</strong></h3>
		<p>Psikiater merupakan dokter spesialis yang telah menyelesaikan pendidikan sarjana strata satu (sarjana kedokteran), pendidikan profesi sebagai dokter dan pendidikan spesialisasi kedokteran jiwa. Namun, yang paling membedakan di sini adalah dalam hal pemberian terapi obat-obatan (farmakoterapi). Terapi obat-obatan ini hanya boleh dilakukan oleh psikiater, yang notabene berlatar belakang kedokteran yang lebih banyak berkecimpung pada penanganan secara klinis. Psikiater atau Dokter Spesialis Kedokteran Jiwa, lebih mendalami tentang kandungan dalam terapi obat-obatan dan telah memiliki izin untuk memberikannya.
		</p>
	</div>
</div>

<div class="row">
	<div class="col-md-12">
		<p>Dalam pelaksanaannya psikolog maupun psikiater dapat bekerja sama dalam menangani pasien. Psikolog dapat merekomendasikan pasien apabila pasien perlu ditangani dengan psikiater maupun sebaliknya. Hal ini tergantung permasalahan maupun gejala yang terjadi pada pasien. Karena berbeda permasalahan/gejala pasti akan berbeda cara menanganinya. 
		</p>
	</div>
</div>

